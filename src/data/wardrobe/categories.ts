function fixed(
  categoryId: WardrobeCategoryId,
  id: string,
  name: string,
  unitPrice: number,
  description?: string,
  imageSrc?: string,
): WardrobeProduct {
  return {
    id,
    categoryId,
    name,
    supplier: "Integral",
    requiresDimensions: false,
    description,
    imageSrc,
    pricing: { type: "fixed", unitPrice },
  };
}

